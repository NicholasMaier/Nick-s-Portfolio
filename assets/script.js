function showCardParagraph1() {
    var cardPara1 = document.getElementById("cardPara1");
    cardPara1.style.display = "block";
}

function hideCardParagraph1() {
    var cardPara1 = document.getElementById("cardPara1");
    cardPara1.style.display = "none";
}

function changeWebsite1Title() {
    var website1Title = document.getElementById("website1Title");
    website1Title.style.color = "var(--backgroundcolor)";
    website1Title.style.backgroundColor = "var(--commoncolor)";
    website1Title.style.boxShadow = "0px 0px 20px 10px var(--commoncolor)";
}

function revertWebsite1Title() {
    var website1Title = document.getElementById("website1Title");
    website1Title.style.color = "var(--commoncolor)";
    website1Title.style.backgroundColor = "var(--backgroundcolor)";
    website1Title.style.boxShadow = "none";
}

